# Literals
'Hello, world!'          // string literal
375                      // integer literal
3.141528                 // float literal
true                     // boolean literal
{ 'a' => 1, 'b' => 2 }   // hash literal
[ 1, 2, 3 ]              // array literal
:sym                     // symbol literal
nil                      // nil literal

# Strings
# Ex. 1: with double quotes
"The man said, 'Hi there!'"

# Ex 2: with single quotes and escaping
'The man said, \'Hi there!\''

# Symbols (Constants)
# Basically, a symbol is used when you want to reference something like a string but don't ever intend to print it to the screen or change it. 
#  It is often referred to as an immutable (i.e. unchangeable) string. While not 100% technically correct, it is a useful mnemonic device for now.
# Examples of symbols
:name
:a_symbol
:"surprisingly, this is also a symbol"

